namespace WebApplication3
{
    public class CalcService
    {
        public int Sum(int A, int B)
        {
            return A + B;
        }

        public int Minus(int A, int B)
        {
            return A - B;
        }

        public int Divide(int A, int B)
        {
            return A / B;
        }

        public int Multiple(int A, int B)
        {
            return A * B;
        }
    }
}
